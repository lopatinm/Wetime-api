    public static function issetUser($username)
    {
        $userObject = User::find()->where(array('username' => $username))->orderBy(['id' => SORT_DESC])->asArray()->One();
        if ($userObject && count($userObject) != 0) {
            return true;
        } else {
            return false;
        }
    }

    public static function findByUsername($username)
